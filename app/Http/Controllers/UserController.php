    public function login(Request $request)
    {
        $request->headers->set('Content-Type', 'application/json');
        $conteudo =  $request->getContent();
        $json = json_decode($conteudo);
        $user = new User();

        Auth::attempt(["login" => $json->login, "password" => $json->password]);
        if (Auth::check()) {
            return "200";
        } else {
            return "401";
        }
    }

