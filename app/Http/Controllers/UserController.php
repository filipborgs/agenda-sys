    {
        $request->headers->set('Content-Type', 'application/json');
        $conteudo =  $request->getContent();
        $json = json_decode($conteudo);

        $user = new User();
        $user->login = $json->login;
        $user->password =  Hash::make($json->password);

        $user->save();
    }

